 package Assignment_Project;

public class Elseif {

	public static void main(String[] args) {

		String dest1 = "Nagpur";
		String dest2 = "Pune";
		
		if(dest1=="Nagpur"){
			System.out.println("Nagpur ticket cost is 1000");
		}
		else if (dest2=="Pune") {
			System.out.println("Pune ticket cost is 2000");
		}
		else {
			System.out.println("No fair");
			
		}
	} 

}
